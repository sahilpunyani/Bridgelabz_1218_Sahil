//comparison operator
//Equal
let a = 5;
let b = '5';
console.log(a == b);

//Strict Equal
let c = 5;
let d = '5';
console.log(c === d);

//Not Equal
let e = 5;
let f = '5';
console.log(e != f);

//Strict Not Equal
let g = 5;
let h = '5';
console.log(g !== h);

//Greater than
let i = 10;
let j = 5;
console.log(i > j);  

//Less than
let k = 10;
let l = 5;
console.log(k < l);  

//Greater than or Equal
let m = 10;
let n = 10;
console.log(m >= n);

//Less than or Equal
